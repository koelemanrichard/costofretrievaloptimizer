export type KeywordCategory = keyof typeof MULTILINGUAL_KEYWORDS;

/**
 * Get all keywords for a category across all languages
 */
export function getAllKeywords(category: KeywordCategory): string[] {
  const keywords = MULTILINGUAL_KEYWORDS[category];
  return Object.values(keywords).flat();
}

/**
 * Get keywords for a specific language and category
 */
export function getKeywordsForLanguage(category: KeywordCategory, lang: string): string[] {
  const langCode = lang.toLowerCase().substring(0, 2);
  const keywords = MULTILINGUAL_KEYWORDS[category];
  return keywords[langCode as keyof typeof keywords] || keywords.en;
}

